#include "../include/push_swap.h"


int is_order(int *stack_a)
{
	int i;

	i = 0;
	while (stack_a[i])
	{
		if (stack_a[i] > stack_a[i + 1])
			i++;
		else
			return (1);
	}
	return (0);
}
